int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int ct;

	i = 1;
	ct = 0;
	while (tab[i] != 0)
	{
		ct = ct + f(tab[i]);
		i++;
	}
	return (ct);
}
